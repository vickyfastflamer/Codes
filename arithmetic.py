def add(x,y):
    sum=x+y
    print("the sum of two values:",sum)
def sub(x,y):
    difference=x-y
    print("the difference between two values:",difference)
def mul(x,y):
    product=x*y
    print("the product of two values:",product)
def div(x,y):
    quotient=x/y
    print("the quotient obtained from division of two values :",quotient)

